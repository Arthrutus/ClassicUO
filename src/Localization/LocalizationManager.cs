        public static string Get(LocalizationProperties prop)
        {
            if (!_dict.TryGetValue(prop, out string result) || string.IsNullOrEmpty(result))
            {
                return "<CUO_ERROR_locals_string_not_found>";
            }

            return result;
        }


        public static void LoadFile(string file)
        {
            FileInfo fileInfo = new FileInfo(Path.Combine(CUOEnviroment.ExecutablePath, "Data", "Languages", file));

            if (!fileInfo.Exists)
            {
                Log.Warn($"localization file not found: '{fileInfo.FullName}'");
            }
            else
            {
                // File format:
                // INDEX=VALUE
                using (StreamReader reader = new StreamReader(File.OpenRead(fileInfo.FullName)))
                {
                    while (!reader.EndOfStream)
                    {
                        ReadOnlySpan<char> line = reader.ReadLine().AsSpan();

                        int index = line.IndexOf('=');

                        if (index > 0)
                        {
                            ReadOnlySpan<char> key = line.Slice(0, index - 1);
                            ReadOnlySpan<char> value = line.Slice(index);

                            if (int.TryParse(key.ToString(), out index) && !value.IsEmpty && index >= 0 && index < (int) LocalizationProperties.COUNT_INTERNAL_USE_ONLY)
                            {
                                _dict[(LocalizationProperties) index] = value.ToString();

                                Log.Debug($"replaced: [{index}] - [{_dict[(LocalizationProperties)index]}]");
                            }
                        }
                    }
                }
            }
        }
        

